namespace GeneXus.Drawing.Drawing2D;

/// <summary>
///  Specifies the type of warp transformation applied in a <see cref='GraphicsPath.Wrap'/> method.
/// </summary>
public enum WarpMode
{
	/// <summary>
	///  Specifies a perspective warp.
	/// </summary>
	Perspective = 0,

	/// <summary>
	///  Specifies a bilinear warp.
	/// </summary>
	Bilinear = 1
}
